function daysAgo(days: number): string {
  const d = new Date("2026-07-01T10:00:00.000Z");
  d.setDate(d.getDate() - days);
  return d.toISOString().slice(0, 10);
}

function buildTx(
  partial: Omit<Transaction, "agingBucket"> & { agingDays: number }
): Transaction {
  return { ...partial, agingBucket: agingBucket(partial.agingDays) };
}

